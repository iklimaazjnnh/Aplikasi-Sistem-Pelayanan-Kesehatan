 protected void autonumber() {
    try {
        String sql = "SELECT no_konsultasi FROM konsultasi ORDER BY no_konsultasi ASC";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        String newNumber = "TRX0001";
        while (rs.next()) {
            String nk = rs.getString("no_konsultasi").substring(3);
            int AN = Integer.parseInt(nk) + 1;
            String Nol = "";

            if (AN < 10) {
                Nol = "000";
            } else if (AN < 100) {
                Nol = "00";
            } else if (AN < 1000) {
                Nol = "0";
            } else if (AN < 10000) {
                Nol = "";
            }

            newNumber = "TRX" + Nol + AN;
        }

        txtnotrx.setText(newNumber);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Auto Number Gagal" + e);
    }
}


