    private void btambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btambahActionPerformed
         try {
    // Kode untuk menambahkan data ke dalam tabel

    String nik_pasien = txtnik.getText();
    String nama = txtnmpasien.getText();
    String id_dokter = txtiddr.getText();
    String nama_dokter = txtnmdr.getText();
    String gejala = txtgejala.getText();
    String tindakan = "";

    if (cbtindakan.getSelectedItem() != null) {
        tindakan = cbtindakan.getSelectedItem().toString();
    }

    tabmode.addRow(new Object[]{nik_pasien, nama, id_dokter, nama_dokter, gejala, tindakan});
    tblkonsultasi.setModel(tabmode);

    // Mengatur ulang nilai teks setelah operasi berhasil
    txtnik.setText("");
    txtnmpasien.setText("");
    txtareaalamat.setText("");
    txtiddr.setText("");
    txtnmdr.setText("");
    txtgejala.setText("");
    cbtindakan.setSelectedIndex(0);
}
catch (Exception e) {
    System.out.println("Error : " + e);
}


    }//GEN-LAST:event_btambahActionPerformed

