    private void tblpasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpasienMouseClicked
        int bar = tblpasien.getSelectedRow();
        if (psn != null){
        psn.nik_pasien = tblpasien.getValueAt(bar, 0).toString();
        psn.nama = tblpasien.getValueAt(bar, 1).toString();
        psn.tgl_lahir = tblpasien.getValueAt(bar, 2).toString();
        psn.jenis_kelamin = tblpasien.getValueAt(bar, 3).toString();
        psn.no_telepon = tblpasien.getValueAt(bar, 4).toString();
        psn.alamat = tblpasien.getValueAt(bar, 5).toString();
        psn.jaminan = tblpasien.getValueAt(bar, 6).toString();
        psn.itemTerpilih();
        }
        
        if (psn2 != null){
        psn2.nik_pasien = tblpasien.getValueAt(bar, 0).toString();
        psn2.nama = tblpasien.getValueAt(bar, 1).toString();
        psn2.alamat = tblpasien.getValueAt(bar, 5).toString();
        psn2.itemTerpilih2();  
        }
        this.dispose();
    }//GEN-LAST:event_tblpasienMouseClicked

