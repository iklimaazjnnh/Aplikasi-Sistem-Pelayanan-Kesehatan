    protected void datatable(){
        Object[] Baris ={"kode_obat","nama_obat","jenis_obat","harga","satuan","stok"};//nama kolom table
            tabmode = new DefaultTableModel(null, Baris);
            //tbl_penduduk.setModel(tabmode);
            String cariitem = txtcari.getText();
            
            try {
                String sql = "SELECT * FROM obat where kode_obat like '%"+ cariitem +"%' or nama_obat like '%"+ cariitem+"%' order by kode_obat asc";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()){
                    tabmode.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6)
                    });
                }
                tblobat.setModel(tabmode);
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }
    
    protected void autonumber(){
        try{
            String sql = "SELECT kode_obat FROM obat order by kode_obat asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txtkd_obat.setText("KD0001");
            while (rs.next()) {
                String kode_obat = rs.getString("kode_obat").substring(2);
                int AN = Integer.parseInt(kode_obat) + 1;
                String Nol = "";
                
                if(AN<10)
                {Nol = "000";}
                else if(AN<100)
                {Nol = "00";}
                else if(AN<1000)
                {Nol = "0";}
                else if(AN<10000)
                {Nol = "";}
                
                txtkd_obat.setText("KD" + Nol + AN);
            }
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Auto Number Gagal" +e);
        }
    }
