    private void tblobatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblobatMouseClicked
        // TODO add your handling code here:
        int bar = tblobat.getSelectedRow();
        String a = tblobat.getValueAt(bar, 0).toString();
        String b = tblobat.getValueAt(bar, 1).toString();
        String c = tblobat.getValueAt(bar, 2).toString();
        String d = tblobat.getValueAt(bar, 3).toString();
        String e = tblobat.getValueAt(bar, 4).toString();
        String f = tblobat.getValueAt(bar, 5).toString();
        
        txtkd_obat.setText(a);
        txtobat.setText(b);
        cbobat.setSelectedItem(c);
        txtharga.setText(d);
        if("BOK".equals(e)){
            rbbox.setSelected(true);
        }else {
            rbstrip.setSelected(true);
            rbbotol.setSelected(true);
        }
        txtstok.setText(f);
    }//GEN-LAST:event_tblobatMouseClicked

