    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String satuan = null;
        if(rbbox.isSelected()){
            satuan = "BOX";
        }else if(rbstrip.isSelected()){
            satuan = "STRIP";
        }else if(rbbotol.isSelected()){
            satuan = "BOTOL";
        }
        String sql = "INSERT INTO obat (kode_obat,nama_obat,jenis_obat,harga,satuan,stok) VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtkd_obat.getText());
            stat.setString(2, txtobat.getText());
            stat.setString(3, cbobat.getSelectedItem().toString());
            stat.setString(4, txtharga.getText());
            stat.setString(5, satuan);
            stat.setString(6, txtstok.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            txtkd_obat.requestFocus();
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
        }
        datatable();
        autonumber();
        aktif();
    }//GEN-LAST:event_bsimpanActionPerformed

