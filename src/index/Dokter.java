    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String jenis = null;
        if(rbumum.isSelected()){
            jenis = "UMUM";
        }else if(rbspesialis.isSelected()){
            jenis = "SPESIALIS";
        }else if(rbgigi.isSelected()){
            jenis = "GIGI";
        }
            String sql = "INSERT INTO dokter (id_dokter,nama_dokter,jenis_dokter,spesialis,jadwal) VALUES (?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, jenis);
            stat.setString(4, cbspesialis.getSelectedItem().toString());
            stat.setString(5, txtjadwal.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            txtnama.requestFocus();
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
        }
        datatable();
        autonumber();
        aktif();
        
    }//GEN-LAST:event_bsimpanActionPerformed

