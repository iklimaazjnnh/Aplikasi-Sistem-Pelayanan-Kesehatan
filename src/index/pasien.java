     protected void datatable(){
        Object[] Baris ={"nik_pasien","nama","tgl_lahir","jenis_kelamin","no_telepon","alamat","jaminan"};//nama kolom table
            tabmode = new DefaultTableModel(null, Baris);
            //tbl_penduduk.setModel(tabmode);
            String cariitem = txtcari.getText();
            try {
                String sql = "SELECT * FROM pasien where nik_pasien like '%"+ cariitem +"%' or nama like '%"+ cariitem+"%' order by nik_pasien asc";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()){
                    tabmode.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                        hasil.getString(7)
                    });
                }
                tblpasien.setModel(tabmode);
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }

