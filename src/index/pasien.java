    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
       String jenis = null;
        if(rblaki.isSelected()){
            jenis = "Laki-Laki";
        }else if(rbperempuan.isSelected()){
            jenis = "Perempuan";
        }
        String sql = "insert into pasien values (?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtnik.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, txtlahir.getText());
            stat.setString(4, jenis);
            stat.setString(5, txttlp.getText());
            stat.setString(6, txtareaalamat.getText());
            stat.setString(7, cbjaminan.getSelectedItem().toString());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            txtnik.requestFocus();
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
        }
        datatable();
        aktif();
    }//GEN-LAST:event_btnsimpanActionPerformed

